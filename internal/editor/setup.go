func setupHelix(migrainePath string) error {
	configDir := helixConfigDir()
	if err := os.MkdirAll(configDir, 0o755); err != nil {
		return fmt.Errorf("create helix config dir: %w", err)
	}

	languagesFile := filepath.Join(configDir, "languages.toml")

	entry := fmt.Sprintf("\n# Migraine LSP (auto-generated by 'migraine init --editor helix')\n[[language]]\nname = \"migraine\"\nscope = \"source.mg\"\nfile-types = [\"mg\"]\nroots = [\"migraine.yaml\"]\ncomment-token = \"#\"\n\n[language-server.migraine-lsp]\ncommand = \"%s\"\nargs = [\"lsp\"]\n", migrainePath)

	var existing []byte
	if data, err := os.ReadFile(languagesFile); err == nil {
		existing = data
	}

	content := string(existing) + entry
	if err := os.WriteFile(languagesFile, []byte(content), 0o644); err != nil {
		return fmt.Errorf("write helix languages.toml: %w", err)
	}

	fmt.Printf("  Helix config written to %s\n", languagesFile)
	return nil
}

func nvimConfigDir() string {
	home, _ := os.UserHomeDir()
	if xdg := os.Getenv("XDG_CONFIG_HOME"); xdg != "" {
		return filepath.Join(xdg, "nvim")
	}
	return filepath.Join(home, ".config", "nvim")
}

func vimConfigDir() string {
	home, _ := os.UserHomeDir()
	return filepath.Join(home, ".vim")
}

func helixConfigDir() string {
	home, _ := os.UserHomeDir()
	if runtime.GOOS == "darwin" {
		return filepath.Join(home, "Library", "Preferences", "helix")
	}
	xdg := os.Getenv("XDG_CONFIG_HOME")
	if xdg != "" {
		return filepath.Join(xdg, "helix")
	}
	return filepath.Join(home, ".config", "helix")
}